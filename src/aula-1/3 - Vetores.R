numeros <- c(1, 2, 3, 4, 5)

#operações

soma <- sum(numeros)     # soma do vetor
media <- mean(numeros)    # média do vetor
tamanho <- length(numeros)  # tamanho do vetor

segunda_posicao_do_vetor <- numeros[2]       # acessar Posição

print(soma)
print(media)
print(tamanho)
print(segunda_posicao_do_vetor)


