numero_1 <- 10
numero_2 <- 100

# Aritméticos
print(numero_1 + numero_2)
print(numero_1 - numero_2)
print(numero_1 * numero_2)
print(numero_2 / numero_1)
print(numero_2 ^ numero_1) # potência 
print(numero_2 %% numero_1) # resto da divisão 
print(numero_2 %/% numero_1) # divisão inteira 

# Relacionais
print(numero_1 == numero_2) # igualdade
print(numero_1 != numero_2) # diferença
print(numero_1 < numero_2) # menor
print(numero_1 > numero_2) # maior
print(numero_1 <= numero_2) # menor ou igual 
print(numero_1 >= numero_2) # maior ou igual 

# Lógicos
print((numero_1 < numero_2) & (numero_1 > 0)) # e 
print((numero_1 < numero_2) | (numero_1 > 100)) # ou
print(!(numero_1 < numero_2)) # negação 

# Atribuição
x <- numero_1 + numero_2 
print(x)